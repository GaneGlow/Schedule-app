package com.example.schedule_app.schedule_elements;

public class InitStudents {

    public int group;
    public int course;

    public void setGroup(int group) {
        this.group = group;
    }

    public void setCourse(int course) {
        this.course = course;
    }

    public InitStudents(int group, int course) {
        this.group = group;
        this.course = course;
    }
}
